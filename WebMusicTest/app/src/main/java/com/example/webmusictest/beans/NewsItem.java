public class NewsItem {
    private String title;
    private String url;
    private String day;

    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }

    public String getDay() {
        return day;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public void setDay(String day) {
        this.day = day;
    }
}
